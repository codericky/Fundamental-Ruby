Apa itu ruby ?

Ruby adalah bahasa pemrograman dinamis berbasis skrip yang berorientasi objek,
Tujuan dari ruby adalah menggabungkan kelebihan dari semua bahasa-bahasa pemrograman skrip yang ada di dunia. Ruby ditulis dengan bahasa pemrograman C, 
dengan kemampuan dasar seperti Perl dan Python, ektensi ruby diakhir dengan rb

Pembuat Ruby : Yukihiro Matsumoto

